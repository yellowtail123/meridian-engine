-- ── generate_meridian_id() — auto-assigns ME-YYYY-NNNN on publication insert ──
create or replace function generate_meridian_id()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  current_year text;
  next_seq int;
begin
  current_year := to_char(now(), 'YYYY');

  select coalesce(max(
    cast(substring(meridian_id from 'ME-' || current_year || '-(\d+)') as int)
  ), 0) + 1
  into next_seq
  from publications
  where meridian_id like 'ME-' || current_year || '-%';

  new.meridian_id := 'ME-' || current_year || '-' || lpad(next_seq::text, 4, '0');
  return new;
end;
$$;

create trigger trg_generate_meridian_id
  before insert on publications
  for each row
  when (new.meridian_id is null)
  execute function generate_meridian_id();

-- ── generate_data_id() — auto-assigns MD-YYYY-NNNN on dataset insert ──
create or replace function generate_data_id()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  current_year text;
  next_seq int;
begin
  current_year := to_char(now(), 'YYYY');

  select coalesce(max(
    cast(substring(meridian_data_id from 'MD-' || current_year || '-(\d+)') as int)
  ), 0) + 1
  into next_seq
  from archived_datasets
  where meridian_data_id like 'MD-' || current_year || '-%';

  new.meridian_data_id := 'MD-' || current_year || '-' || lpad(next_seq::text, 4, '0');
  return new;
end;
$$;

create trigger trg_generate_data_id
  before insert on archived_datasets
  for each row
  when (new.meridian_data_id is null)
  execute function generate_data_id();

-- ── increment_endorsement() — inserts endorsement + increments count ──
create or replace function increment_endorsement(pub_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  -- Insert endorsement (unique constraint prevents duplicates)
  insert into publication_endorsements (publication_id, user_id)
  values (pub_id, auth.uid());

  -- Increment counter on the publication
  update publications
  set endorsement_count = endorsement_count + 1
  where id = pub_id;
end;
$$;

-- ── remove_endorsement() — removes endorsement + decrements count ──
create or replace function remove_endorsement(pub_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  delete from publication_endorsements
  where publication_id = pub_id and user_id = auth.uid();

  if found then
    update publications
    set endorsement_count = greatest(0, endorsement_count - 1)
    where id = pub_id;
  end if;
end;
$$;

-- ── increment_download() — increments download_count on archived_datasets ──
create or replace function increment_download(dataset_id uuid)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  update archived_datasets
  set download_count = download_count + 1
  where id = dataset_id;
end;
$$;

-- ── Auto-update updated_at on publications ──
create or replace function update_publications_timestamp()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

create trigger trg_publications_updated_at
  before update on publications
  for each row
  execute function update_publications_timestamp();

-- ── Auto-set is_trusted_author on publication insert ──
create or replace function set_trusted_author_flag()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  new.is_trusted_author := exists (
    select 1 from trusted_authors where user_id = new.user_id
  );
  return new;
end;
$$;

create trigger trg_set_trusted_author
  before insert on publications
  for each row
  execute function set_trusted_author_flag();

-- ── Auto-update flag_count when a flag is inserted ──
create or replace function update_flag_count()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  update publications
  set flag_count = (
    select count(*) from publication_flags where publication_id = new.publication_id
  )
  where id = new.publication_id;
  return new;
end;
$$;

create trigger trg_update_flag_count
  after insert on publication_flags
  for each row
  execute function update_flag_count();


