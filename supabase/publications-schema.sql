-- Meridian Engine — Publications & Archived Datasets Schema
-- Run this in the Supabase SQL Editor (Dashboard > SQL Editor > New Query)
-- Requires: auth.users, user_roles table, is_admin() function (from admin.sql)

-- ═══════════════════════════════════════════════════════════
-- 1. TABLES
-- ═══════════════════════════════════════════════════════════

-- ── Publications ──
create table publications (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users not null,
  title text not null,
  authors jsonb not null,  -- [{name, affiliation, email, orcid}]
  collaborators jsonb,     -- [{name, type, country}]
  abstract text not null,
  keywords text[],
  category text not null check (category in (
    'Field Study', 'Technical Report', 'Thesis/Dissertation', 'Review',
    'Dataset Description', 'Monitoring Report', 'Methodology',
    'Policy Brief', 'Preprint', 'Other'
  )),
  location_region text,
  location_lat float8,
  location_lng float8,
  species_studied text[],
  time_frame_start date,
  time_frame_end date,
  pdf_url text,
  word_count integer,
  sections_detected jsonb,  -- {abstract, introduction, methods, results, discussion, conclusion, references}
  reference_count integer,
  quality_checks jsonb,     -- stores all automated check results
  meridian_id text unique,  -- format ME-YYYY-NNNN, auto-generated by trigger
  status text not null default 'pending_review' check (status in (
    'draft', 'pending_review', 'published', 'flagged', 'removed'
  )),
  endorsement_count integer not null default 0,
  flag_count integer not null default 0,
  is_trusted_author boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  linked_dataset_ids uuid[]
);

-- ── Publication Comments ──
create table publication_comments (
  id uuid default gen_random_uuid() primary key,
  publication_id uuid references publications on delete cascade not null,
  user_id uuid references auth.users not null,
  comment_text text not null,
  created_at timestamptz not null default now()
);

-- ── Publication Flags ──
create table publication_flags (
  id uuid default gen_random_uuid() primary key,
  publication_id uuid references publications not null,
  user_id uuid references auth.users not null,
  reason text not null check (reason in (
    'plagiarism', 'misleading_data', 'insufficient_methods',
    'factual_errors', 'spam', 'other'
  )),
  description text,
  created_at timestamptz not null default now()
);

-- ── Publication Endorsements ──
create table publication_endorsements (
  id uuid default gen_random_uuid() primary key,
  publication_id uuid references publications on delete cascade not null,
  user_id uuid references auth.users not null,
  created_at timestamptz not null default now(),
  unique (publication_id, user_id)
);

-- ── Trusted Authors ──
create table trusted_authors (
  user_id uuid references auth.users primary key,
  approved_by uuid references auth.users,
  approved_at timestamptz not null default now()
);

-- ── Archived Datasets ──
create table archived_datasets (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users not null,
  title text not null,
  description text not null,
  authors jsonb,  -- [{name, affiliation, email, orcid}]
  data_type text not null check (data_type in (
    'Catch Data', 'Survey Data', 'Environmental Monitoring',
    'Genetic/Genomic', 'Acoustic/Telemetry', 'Photo/Video Transect',
    'Water Quality', 'Population Census', 'Tagging/Recapture',
    'Socioeconomic', 'Other'
  )),
  file_format text check (file_format in (
    'CSV', 'XLSX', 'JSON', 'NetCDF', 'R Data', 'ZIP', 'Other'
  )),
  file_url text,
  file_size_bytes bigint,
  region text,
  location_lat float8,
  location_lng float8,
  species text[],
  time_frame_start date,
  time_frame_end date,
  variables_included text[],  -- e.g. SST, Length, Weight, Depth, Salinity
  collection_method text,
  sample_size integer,
  license text check (license in ('CC-BY', 'CC-BY-NC', 'CC-BY-SA', 'CC0', 'Custom')),
  linked_publication_id uuid references publications,
  meridian_data_id text unique,  -- format MD-YYYY-NNNN, auto-generated by trigger
  download_count integer not null default 0,
  created_at timestamptz not null default now(),
  status text not null default 'published' check (status in (
    'published', 'draft', 'removed'
  ))
);


-- ═══════════════════════════════════════════════════════════
-- 2. STORAGE BUCKETS
-- ═══════════════════════════════════════════════════════════

insert into storage.buckets (id, name, public, file_size_limit)
values
  ('publication-pdfs', 'publication-pdfs', true, 52428800),      -- 50 MB
  ('archived-datasets', 'archived-datasets', true, 209715200)   -- 200 MB
on conflict (id) do nothing;

-- Storage policies: public read, authenticated upload
create policy "Public read publication PDFs"
  on storage.objects for select
  using (bucket_id = 'publication-pdfs');

create policy "Authenticated upload publication PDFs"
  on storage.objects for insert
  with check (bucket_id = 'publication-pdfs' and auth.role() = 'authenticated');

create policy "Users delete own publication PDFs"
  on storage.objects for delete
  using (bucket_id = 'publication-pdfs' and auth.uid()::text = (storage.foldername(name))[1]);

create policy "Public read archived datasets"
  on storage.objects for select
  using (bucket_id = 'archived-datasets');

create policy "Authenticated upload archived datasets"
  on storage.objects for insert
  with check (bucket_id = 'archived-datasets' and auth.role() = 'authenticated');

create policy "Users delete own archived datasets"
  on storage.objects for delete
  using (bucket_id = 'archived-datasets' and auth.uid()::text = (storage.foldername(name))[1]);


