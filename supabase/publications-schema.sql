-- 3. ROW LEVEL SECURITY
-- ═══════════════════════════════════════════════════════════

alter table publications enable row level security;
alter table publication_comments enable row level security;
alter table publication_flags enable row level security;
alter table publication_endorsements enable row level security;
alter table trusted_authors enable row level security;
alter table archived_datasets enable row level security;

-- ── Publications RLS ──

-- Anyone can read published publications
create policy "Anyone can read published publications"
  on publications for select
  using (status = 'published' or auth.uid() = user_id or is_admin());

-- Authenticated users can create publications
create policy "Authenticated users can create publications"
  on publications for insert
  with check (auth.uid() = user_id);

-- Users can update their own draft/pending publications; admins can update any
create policy "Users update own draft/pending; admins update any"
  on publications for update
  using (
    (auth.uid() = user_id and status in ('draft', 'pending_review'))
    or is_admin()
  );

-- Users can delete their own draft/pending publications; admins can delete any
create policy "Users delete own draft/pending; admins delete any"
  on publications for delete
  using (
    (auth.uid() = user_id and status in ('draft', 'pending_review'))
    or is_admin()
  );

-- ── Publication Comments RLS ──

create policy "Anyone can read comments on published pubs"
  on publication_comments for select
  using (
    exists (
      select 1 from publications p
      where p.id = publication_id and (p.status = 'published' or p.user_id = auth.uid())
    ) or is_admin()
  );

create policy "Authenticated users can comment"
  on publication_comments for insert
  with check (auth.uid() = user_id);

create policy "Users delete own comments; admins delete any"
  on publication_comments for delete
  using (auth.uid() = user_id or is_admin());

-- ── Publication Flags RLS ──

create policy "Users can read own flags; admins read all"
  on publication_flags for select
  using (auth.uid() = user_id or is_admin());

create policy "Authenticated users can flag"
  on publication_flags for insert
  with check (auth.uid() = user_id);

create policy "Admins can delete flags"
  on publication_flags for delete
  using (is_admin());

-- ── Publication Endorsements RLS ──

create policy "Anyone can read endorsements"
  on publication_endorsements for select
  using (true);

create policy "Authenticated users can endorse"
  on publication_endorsements for insert
  with check (auth.uid() = user_id);

create policy "Users can remove own endorsement"
  on publication_endorsements for delete
  using (auth.uid() = user_id);

-- ── Trusted Authors RLS ──

create policy "Anyone can read trusted authors"
  on trusted_authors for select
  using (true);

create policy "Admins manage trusted authors"
  on trusted_authors for insert
  with check (is_admin());

create policy "Admins remove trusted authors"
  on trusted_authors for delete
  using (is_admin());

-- ── Archived Datasets RLS ──

create policy "Anyone can read published datasets"
  on archived_datasets for select
  using (status = 'published' or auth.uid() = user_id or is_admin());

create policy "Authenticated users can create datasets"
  on archived_datasets for insert
  with check (auth.uid() = user_id);

create policy "Users update own datasets; admins update any"
  on archived_datasets for update
  using (auth.uid() = user_id or is_admin());

create policy "Users delete own datasets; admins delete any"
  on archived_datasets for delete
  using (auth.uid() = user_id or is_admin());


